        if (\Auth::user()->hasRole(['admin', 'medewerker'])) {
            $projects = Project::all();
        } else {
            $projects = \Auth::user()->projects;
        }

        $reports = 0;
        foreach ($projects as $project) {
            $reports += $project->reports()->count();
        }

        
        return view('welcome')->withReports($reports);
    }

